<?php

namespace App\Filament\Blog\Resources;

use App\Filament\Blog\Resources\CategoryResource\Pages\CreateCategory;
use App\Filament\Blog\Resources\CategoryResource\Pages\EditCategory;
use App\Filament\Blog\Resources\CategoryResource\Pages\ListCategories;
use App\Filament\Blog\Resources\CategoryResource\RelationManagers\CategoryAccessRelationManager;
use App\Filament\Blog\Resources\CategoryResource\Schemas\CategoryForm;
use App\Filament\Blog\Resources\CategoryResource\Tables\CategoriesTable;
use App\Models\Category;
use BackedEnum;
use Filament\Resources\Resource;
use Filament\Schemas\Schema;
use Filament\Support\Icons\Heroicon;
use Filament\Tables\Table;
use Illuminate\Database\Eloquent\SoftDeletingScope;
use UnitEnum;

class CategoryResource extends Resource
{
    protected static ?string $model = Category::class;

    protected static string|BackedEnum|null $navigationIcon = Heroicon::OutlinedFolder;

    protected static string|UnitEnum|null $navigationGroup = 'Blog Management';

    protected static ?int $navigationSort = 1;

    public static function form(Schema $schema): Schema
    {
        return CategoryForm::configure($schema);
    }

    public static function table(Table $table): Table
    {
        return CategoriesTable::configure($table);
    }

    public static function getRelations(): array
    {
        return [
            CategoryAccessRelationManager::class,
        ];
    }

    public static function getPages(): array
    {
        return [
            'index' => ListCategories::route('/'),
            'create' => CreateCategory::route('/create'),
            'edit' => EditCategory::route('/{record}/edit'),
        ];
    }

    public static function getEloquentQuery(): \Illuminate\Database\Eloquent\Builder
    {
        return parent::getEloquentQuery()
            ->withoutGlobalScopes([
                SoftDeletingScope::class,
            ]);
    }
}
